public class Main1 {
  // 4-1 点数管理プログラム
  public static void main(String[] args) {
    int sansu = 20;
    int kougo = 30;
    int rika = 40;
    int eigo = 50;
    int syakai = 80;
    int sum = sansu + kougo + rika + eigo + syakai;
    int avg = sum / 5;
    System.out.println("合計点：" + sum);
    System.out.println("平均点：" + avg);
  }
}
